package week1;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int a=30;
        int b=20;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));


    }
}
